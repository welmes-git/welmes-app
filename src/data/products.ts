// Helper: generate standard 3-tier set options from a single unit wholesale price
function makeSets(unitWholesale: number, unitOriginal: number): SetOption[] {
  return [
    {
      id: 'S1',
      description: 'Single Unit',
      unitsPerSet: 1,
      wholesalePrice: unitWholesale,
      originalPrice: unitOriginal,
    },
    {
      id: 'S2',
      description: 'Box of 12 Units',
      unitsPerSet: 12,
      wholesalePrice: Math.round(unitWholesale * 12 * 0.97 / 100) * 100,
      originalPrice: unitOriginal * 12,
    },
    {
      id: 'S3',
      description: 'Case of 24 Units',
      unitsPerSet: 24,
      wholesalePrice: Math.round(unitWholesale * 24 * 0.94 / 100) * 100,
      originalPrice: unitOriginal * 24,
    },
  ];
}

